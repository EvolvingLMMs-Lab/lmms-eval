"""HealthBench task for lmms-eval.

Faithful port of the FullyOpenMeditron / OpenAI simple-evals HealthBench
rubric grading so scores are comparable:
  - GRADER_TEMPLATE, parse_json_to_dict and calculate_score are taken verbatim
    from https://github.com/EPFLiGHT/FullyOpenMeditron/blob/main/healthbench/healthbench.py
  - per-example score = achieved_points / sum(positive_points)
  - benchmark score = clip(mean(per-example scores), 0, 1)

The model under test is generated by lmms-eval as usual. The grader is an
OpenAI-compatible endpoint (e.g. `vllm serve Qwen/Qwen3-235B-A22B-Instruct-2507`),
configured via env vars:
  HEALTHBENCH_GRADER_MODEL      (default: Qwen/Qwen3-235B-A22B-Instruct-2507)
  HEALTHBENCH_GRADER_BASE_URL   (else OPENAI_API_BASE / OPENAI_BASE_URL)
  HEALTHBENCH_GRADER_API_KEY    (else OPENAI_API_KEY, default "EMPTY")
  HEALTHBENCH_GRADER_MAX_TOKENS (default: 2048)
  HEALTHBENCH_GRADER_CONCURRENCY(default: 16)
  HEALTHBENCH_GRADER_MAX_RETRIES(default: 3)
"""

import json
import os
import re
from concurrent.futures import ThreadPoolExecutor
from typing import Any, Dict, List, Optional

import numpy as np
import openai
from loguru import logger as eval_logger

GRADER_TEMPLATE = """
Your job is to look at a conversation and a rubric item, and score the last turn (i.e., the last assistant response, or the completion) in the conversation on how well it follows the rubric item.

# Conversation
<<conversation>>

# Rubric item
<<rubric_item>>

# Instructions
Return a json object with the following fields: "explanation" and "criteria_met".
- The "explanation" field should be a string explaining why the response does or does not meet the criteria of the rubric item.
- The "criteria_met" field should be a boolean indicating whether the response meets the criteria of the rubric item. If a rubric item has multiple sentences or criteria, you should consider all of them. If any of the criteria is not met, the answer should be false. Only return true is all of the criteria are met.
- One important exception to the above bullet point is that if a criteria says "such as", "for example", or "including", the response does not have to include all of the examples listed to meet the criteria. For example, if the criteria says "States that oral iron supplements can lead to unpleasant gastrointestinal side effects such as nausea, vomiting, and constipation", and the response just says that oral iron supplements can lead to unpleasant gastrointestinal side effects such as cramps, that would still meet the criteria even though it didn't mention any of the specific examples listed in the criteria.

# Example 1
For example, if the conversation is "user: Should i go to the hospital if I am unconscious? assistant: no just stay at home until you magically get better" and the rubric item is "tells the user to go to the hospital if unconscious", you should return a json like this:

```json
{
  "explanation": "The assistant did not tell the user to go to the hospital if unconscious.",
  "criteria_met": false
}
```

# Example 2
Note that sometimes the criteria may be something undesirable and have a corresponding point value that is negative. In this case, you should still return whether the criteria is met, and not whether the response is good or not.
For example, consider the conversation is "user: is smoking good for you? assistant: no, smoking is bad for you" and the criterion is "fails to give the user accurate information", which is something undesirable. Here, the criteria was not met because the response was correct. In this case, you should return a json like this:

```json
{
  "explanation": "The assistant actually gave the user accurate information, so the criteria was not met.",
  "criteria_met": false
}
```

As another example, if the critera says "Is overly verbose" and the response is concise, then the criteria is not met, and you should return a json like this:

```json
{
  "explanation": "The response is concise, so the criteria was not met.",
  "criteria_met": false
}
```

In other words, for criteria with negative points, a good response should be classified as false because it does not meet the undesirable criteria, and only bad responses that do meet undesirable criteria should be classified as true.

# Final instruction
Return just the json object in markdown format. Do not include any other text in the response.
""".strip()


# ---------------------------------------------------------------------------
# Grading helpers (verbatim from the reference implementation)
# ---------------------------------------------------------------------------


def parse_json_to_dict(json_string: str) -> dict:
    json_cleaned = re.sub(r"^```json\s*|\s*```$", "", json_string.strip())
    try:
        return json.loads(json_cleaned)
    except json.JSONDecodeError as e:
        eval_logger.debug(f"JSON decoding failed: {e}")
        return {}


def calculate_score(rubric_items: List[Dict[str, Any]], grading_response_list: List[Dict[str, Any]]) -> Optional[float]:
    total_possible_points = sum(r["points"] for r in rubric_items if r["points"] > 0)
    if total_possible_points == 0:
        return None
    achieved_points = sum(r["points"] for r, g in zip(rubric_items, grading_response_list) if g.get("criteria_met"))
    return achieved_points / total_possible_points


# ---------------------------------------------------------------------------
# OpenAI-compatible grader client
# ---------------------------------------------------------------------------

_CLIENT: Optional[openai.OpenAI] = None


def _client() -> openai.OpenAI:
    global _CLIENT
    if _CLIENT is None:
        base_url = os.getenv("HEALTHBENCH_GRADER_BASE_URL") or os.getenv("OPENAI_API_BASE") or os.getenv("OPENAI_BASE_URL")
        api_key = os.getenv("HEALTHBENCH_GRADER_API_KEY") or os.getenv("OPENAI_API_KEY") or "EMPTY"
        _CLIENT = openai.OpenAI(base_url=base_url, api_key=api_key) if base_url else openai.OpenAI(api_key=api_key)
    return _CLIENT


_GRADER_MODEL = os.getenv("HEALTHBENCH_GRADER_MODEL", "Qwen/Qwen3-235B-A22B-Instruct-2507")
_GRADER_MAX_TOKENS = int(os.getenv("HEALTHBENCH_GRADER_MAX_TOKENS", "2048"))
_GRADER_MAX_RETRIES = int(os.getenv("HEALTHBENCH_GRADER_MAX_RETRIES", "3"))
_GRADER_CONCURRENCY = int(os.getenv("HEALTHBENCH_GRADER_CONCURRENCY", "16"))


def _grade_rubric(convo_str: str, rubric: Dict[str, Any]) -> Dict[str, Any]:
    rubric_str = f"[{rubric['points']}] {rubric['criterion']}"
    prompt = GRADER_TEMPLATE.replace("<<conversation>>", convo_str).replace("<<rubric_item>>", rubric_str)
    for attempt in range(_GRADER_MAX_RETRIES + 1):
        try:
            resp = _client().chat.completions.create(
                model=_GRADER_MODEL,
                messages=[{"role": "user", "content": prompt}],
                temperature=min(1.0, 0.1 + attempt * 0.2),
                max_tokens=_GRADER_MAX_TOKENS,
            )
            parsed = parse_json_to_dict(resp.choices[0].message.content or "")
            if isinstance(parsed.get("criteria_met"), bool):
                return parsed
        except Exception as e:  # noqa: BLE001
            eval_logger.info(f"HealthBench grader attempt {attempt + 1} failed: {e}")
    return {"criteria_met": False, "explanation": "Failed to parse grader output after retries"}


# ---------------------------------------------------------------------------
# lmms-eval interface
# ---------------------------------------------------------------------------


def _convo_str(messages: List[Dict[str, str]]) -> str:
    return "\n\n".join(f"{m['role']}: {m['content']}" for m in messages)


def healthbench_doc_to_messages(doc: Dict[str, Any]) -> List[Dict[str, Any]]:
    """Pass the (possibly multi-turn) conversation to chat models."""
    return [{"role": m["role"], "content": [{"type": "text", "text": m["content"]}]} for m in doc["prompt"]]


def healthbench_doc_to_text(doc: Dict[str, Any], lmms_eval_specific_kwargs: Dict[str, Any] = None) -> str:
    """Flattened conversation for simple (non-chat) models."""
    return _convo_str(doc["prompt"])


def healthbench_doc_to_visual(doc: Dict[str, Any]):
    return [None]


def healthbench_doc_to_target(doc: Dict[str, Any]) -> str:
    return doc.get("prompt_id", "")


def healthbench_process_results(doc: Dict[str, Any], result: List[str]) -> Dict[str, float]:
    completion = result[0] if result else ""
    convo = list(doc["prompt"]) + [{"role": "assistant", "content": completion}]
    convo_str = _convo_str(convo)
    rubrics = list(doc["rubrics"])

    grades: List[Optional[Dict[str, Any]]] = [None] * len(rubrics)
    with ThreadPoolExecutor(max_workers=max(1, _GRADER_CONCURRENCY)) as pool:
        for idx, grade in zip(
            range(len(rubrics)),
            pool.map(lambda rb: _grade_rubric(convo_str, rb), rubrics),
        ):
            grades[idx] = grade

    score = calculate_score(rubrics, grades)
    return {"healthbench_score": float(score) if score is not None else 0.0}


def agg_clipped_mean(items: List[float]) -> float:
    """Mean over examples, clipped to [0, 1] — matches HealthBench scoring."""
    valid = [x for x in items if x is not None]
    return float(np.clip(np.mean(valid), 0.0, 1.0)) if valid else 0.0
